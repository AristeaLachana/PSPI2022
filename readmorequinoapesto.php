Χτυπάμε στο μπλέντερ την ρόκα, τον δυόσμο, το σκόρδο, το κουκουνάρι, το 1/4 φλ. λάδι, 1/4 κ.γ αλάτι και το τυρί, μέχρι να προκύψει ένα ωραίο πέστο. Μεταφέρουμε σε μπολ.
Σε μια μεγάλη κατσαρόλα ζεσταίνουμε τα 2 κ.γ. λάδι σε μέτρια φωτιά και σοτάρουμε τα φρέσκα κρεμμυδάκια για 2 λεπτά, ανακατεύοντας κατά διαστήματα.
Προσθέτουμε την κινόα, σοτάρουμε για 1 λεπτό και συμπληρώνουμε με 2 φλ. ζεστό νερό ή ζωμό λαχανικών και 1⁄2 κ.γ αλάτι. Χαμηλώνουμε την θερμοκρασία και σιγοβράζουμε μέχρι να απορροφηθεί το μεγαλύτερο μέρος του νερού, ανακατεύοντας κατά διαστήματα, για περίπου 6 – 8 λεπτά. Προσθέτουμε άλλα 2 φλ. ζεστό νερό ή ζωμό και σιγοβράζουμε μέχρι να μαλακώσει η κινόα, ανακατεύοντας κατά διαστήματα, για 15 – 18 λεπτά.
Προσθέτουμε το βούτυρο, τον χυμό λεμονιού, το μισό πέστο και 1⁄2 φλ. νερό, ανακατεύοντας καλά. Προσθέτουμε τα ρεβίθια και ανακατεύουμε απαλά.
Σερβίρουμε το μείγμα κινόα σε πιατέλα, περιχύνουμε με το υπόλοιπο πέστο και γαρνίρουμε με μυρωδικά και κουκουνάρι, αν θέλουμε.
